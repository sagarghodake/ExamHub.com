package com.examhub.pojo;

public class Assignment {
	private int assignementId;
	private String assignmentData;
	private String subjectName;
	
	
	
	public int getAssignementId() {
		return assignementId;
	}
	public void setAssignementId(int assignementId) {
		this.assignementId = assignementId;
	}
	public String getAssignmentData() {
		return assignmentData;
	}
	public void setAssignmentData(String assignmentData) {
		this.assignmentData = assignmentData;
	}
	public String getSubjectName() {
		return subjectName;
	}
	public void setSubjectName(String subjectName) {
		this.subjectName = subjectName;
	}
	
}
